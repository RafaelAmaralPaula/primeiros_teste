
public class Operador2 {
	
	public static void main(String[] args) {
		
		int total = 10;
		total = total + 3;
		System.out.println(total);
		
		int total1 = 10;
		
		total1 += 3;
		System.out.println(total1);
		
		total1 -= 2;
		System.out.println(total1);
		
		total1 *= 3;
		System.out.println(total1);
		
		total1 /= 3;
		System.out.println(total1);
		
		total1 %= 5;
		System.out.println(total1);
		
	}
}
