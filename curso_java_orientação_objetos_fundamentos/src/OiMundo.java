public class OiMundo {

	public static void main(String[] args) {

		// A linha abaixo envia uma mensagem para o monitor
		System.out.println("Oi Mundo");

		/*
		 * Essa linha será ignorada pelo compilador System.out.printl(""); e essa também
		 */

		System.out.println("Oi Mundo");

		System.out.println("");

		System.out.println("Oi \"Mundo\"");

		System.out.println("");

		System.out.println("Oi \\Mundo\\");

		System.out.println("");

		System.out.println("Oi \nMundo");

	}

}